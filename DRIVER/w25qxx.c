//擦除整个芯片		  
//等待时间超长...
void W25QXX_Erase_Chip(void)   
{                                   
	W25QXX_Write_Enable();                 	 	//SET WEL 
	W25QXX_Wait_Busy();   
	W25QXX_CS = 0;                           	//使能器件   
	SPI3_ReadWriteByte(W25X_ChipErase);        	//发送片擦除命令  
	W25QXX_CS = 1;                            	//取消片选     	      
	W25QXX_Wait_Busy();   				   		//等待芯片擦除结束
}   
//擦除一个扇区
//Dst_Addr:扇区地址 根据实际容量设置
//擦除一个山区的最少时间:150ms
void W25QXX_Erase_Sector(u32 Dst_Addr)   
{  
	//监视falsh擦除情况,测试用   
// 	printf("fe:%x\r\n",Dst_Addr);	  			//可删除？张成祥
	Dst_Addr *= 4096;
	W25QXX_Write_Enable();                  	//SET WEL 	 
	W25QXX_Wait_Busy();   
	W25QXX_CS = 0;                            	//使能器件   
	SPI3_ReadWriteByte(W25X_SectorErase);      	//发送扇区擦除指令 
	SPI3_ReadWriteByte((u8)((Dst_Addr)>>16));  	//发送24bit地址    
	SPI3_ReadWriteByte((u8)((Dst_Addr)>>8));   
	SPI3_ReadWriteByte((u8)Dst_Addr);  
	W25QXX_CS = 1;                            	//取消片选     	      
	W25QXX_Wait_Busy();   				   		//等待擦除完成
}

//等待空闲
void W25QXX_Wait_Busy(void)
{   
	while((W25QXX_ReadSR()&0x01)==0x01);  		// 等待BUSY位清空
}

//进入掉电模式
void W25QXX_PowerDown(void)   
{ 
	W25QXX_CS = 0;                           	 	//使能器件   
	SPI3_ReadWriteByte(W25X_PowerDown);        //发送掉电命令  
	W25QXX_CS = 1;                            	//取消片选     	      
	delay_us(3);	//等待TPD  
}

//唤醒
void W25QXX_WAKEUP(void)   
{  
	W25QXX_CS = 0;                            	//使能器件   
	SPI3_ReadWriteByte(W25X_ReleasePowerDown);	//  send W25X_PowerDown command 0xAB    
	W25QXX_CS = 1;                            	//取消片选     	      
	delay_us(3);                           	//等待TRES1
}   

/**************************************************************************************************
**  函数名：  W25QXX_Check()
**	输入参数：无
**	输出参数：检测结果 1：检测失败；0：检测成功
**	函数功能：检测芯片是否正常
**	备注：	  对芯片最后一个地址储存标志字，进行校验
**  作者：    
**  开发日期：
***************************************************************************************************/
u8 W25QXX_Check(void)
{
	u8 temp = 0;
	u8 tempone = SAVE_PROTECT_VALUE;
	u32 i = 0;
	
	//避免每次开机都写W25QXX
	W25QXX_Read(&temp, SAVE_PROTECT_END_ADDRESS, 1);
	if(temp == SAVE_PROTECT_VALUE)
	{
		return 0;
	}
	else
	{//排除第一次初始化的情况
		//第二次确认，第一次读时会进行保护区域的参数恢复
		W25QXX_Read(&temp, SAVE_PROTECT_END_ADDRESS, 1);
		if(temp == SAVE_PROTECT_VALUE)
		{
			return 0;
		}
		else
		{
			W25QXX_Erase_Chip();
			W25QXX_Write(&tempone, SAVE_PROTECT_END_ADDRESS, 1);
			W25QXX_Read(&temp, SAVE_PROTECT_END_ADDRESS, 1);
			if(temp == SAVE_PROTECT_VALUE)
			{
				for(i=0; i<OUTPUT_NUM - 4; i++)
				{
					SetOutput(i);
				}
				return 0;
			}
		}
	}
	return 1;											  
}

/**************************************************************************************************
**  函数名：  W25QXX_Writ0_4K()
**	输入参数：无
**	输出参数：
**	函数功能：地址WriteAddr开始写4K字节数据0
**	备注：	  
**  作者：    
**  开发日期：
***************************************************************************************************/
void W25QXX_Writ0_4K(u32 WriteAddr)
{	
	u16 i = 0;
	u32 secpos = 0;
	
	for(i=0; i<4096; i++)
	{ 
			W25QXX_BUFFER[i] = 0;
	}
	
 	secpos = WriteAddr / 4096;//扇区地址
	
	W25QXX_Erase_Sector(secpos);		//擦除这个扇区
	
	if(secpos*4096 == SAVE_PROTECT_START_ADDRESS)
	{//保护区域写入校验字节数值
		W25QXX_BUFFER[4096 - 1] = SAVE_PROTECT_VALUE;
	}
	
	W25QXX_Write_NoCheck(W25QXX_BUFFER, secpos*4096, 4096);//写入整个扇区
	
	if(secpos*4096 == SAVE_PROTECT_START_ADDRESS)
	{
		W25QXX_Erase_Sector(SAVE_PROTECT_BACKUP_ADDRESS/4096);										//擦除保护区域的备份扇区
		W25QXX_Write_NoCheck(W25QXX_BUFFER, SAVE_PROTECT_BACKUP_ADDRESS, 4096);			//写入保护区域的备份扇区
	}
}

