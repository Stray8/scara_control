//初始化SPI FLASH的IO口
void W25QXX_Init(void)
{	
	GPIO_InitTypeDef GPIO_InitStructure;
	
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOA, ENABLE);
	
	//GPIO_PinAFConfig(GPIOA, GPIO_PinSource15, GPIO_AF_SPI3);

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15; 
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
 	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP; 							//推挽复用输出
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP; 								//上拉
 	GPIO_Init(GPIOA, &GPIO_InitStructure);
 
	W25QXX_CS=1;			//SPI FLASH不选中
	SPI3_Init();		   	//初始化SPI
}   

//读取W25QXX的状态寄存器
//BIT7  6   5   4   3   2   1   0
//SPR   RV  TB BP2 BP1 BP0 WEL BUSY
//SPR:默认0,状态寄存器保护位,配合WP使用
//TB,BP2,BP1,BP0:FLASH区域写保护设置
//WEL:写使能锁定
//BUSY:忙标记位(1,忙;0,空闲)
//默认:0x00
u8 W25QXX_ReadSR(void)   
{  
	u8 byte=0;   
	W25QXX_CS=0;                            //使能器件   
	SPI3_ReadWriteByte(W25X_ReadStatusReg); //发送读取状态寄存器命令
	byte=SPI3_ReadWriteByte(0Xff);          //读取一个字节  
	W25QXX_CS=1;                            //取消片选     
	return byte;   
} 
//写W25QXX状态寄存器
//只有SPR,TB,BP2,BP1,BP0(bit 7,5,4,3,2)可以写!!!
void W25QXX_Write_SR(u8 sr)   
{   
	W25QXX_CS=0;                            //使能器件   
	SPI3_ReadWriteByte(W25X_WriteStatusReg);//发送写取状态寄存器命令    
	SPI3_ReadWriteByte(sr);               	//写入一个字节  
	W25QXX_CS=1;                            //取消片选     	      
}   
//W25QXX写使能	
//将WEL置位   
void W25QXX_Write_Enable(void)   
{
	W25QXX_CS=0;                          	//使能器件   
  SPI3_ReadWriteByte(W25X_WriteEnable); 	//发送写使能  
	W25QXX_CS=1;                           	//取消片选     	      
} 
//W25QXX写禁止	
//将WEL清零  
void W25QXX_Write_Disable(void)   
{  
	W25QXX_CS=0;                            //使能器件   
  SPI3_ReadWriteByte(W25X_WriteDisable);  //发送写禁止指令    
	W25QXX_CS=1;                            //取消片选     	      
} 		
//读取芯片ID
//返回值如下:				   
//0XEF13,表示芯片型号为W25Q80  
//0XEF14,表示芯片型号为W25Q16    
//0XEF15,表示芯片型号为W25Q32  
//0XEF16,表示芯片型号为W25Q64 
//0XEF17,表示芯片型号为W25Q128 	  
u16 W25QXX_ReadID(void)
{
	u16 Temp = 0;	  
	W25QXX_CS=0;				    
	SPI3_ReadWriteByte(0x90);//发送读取ID命令	    
	SPI3_ReadWriteByte(0x00); 	    
	SPI3_ReadWriteByte(0x00); 	    
	SPI3_ReadWriteByte(0x00); 	 			   
	Temp|=SPI3_ReadWriteByte(0xFF)<<8;  
	Temp|=SPI3_ReadWriteByte(0xFF);	 
	W25QXX_CS=1;				    
	return Temp;
}   		    

//SPI在一页(0~65535)内写入少于256个字节的数据
//在指定地址开始写入最大256字节的数据
//pBuffer:数据存储区
//WriteAddr:开始写入的地址(24bit)
//NumByteToWrite:要写入的字节数(最大256),该数不应该超过该页的剩余字节数!!!	 
void W25QXX_Write_Page(u8* pBuffer,u32 WriteAddr,u16 NumByteToWrite)
{
 	u16 i;  
	W25QXX_Write_Enable();                  	//SET WEL 
	W25QXX_CS=0;                            	//使能器件   
	SPI3_ReadWriteByte(W25X_PageProgram);      	//发送写页命令   
	SPI3_ReadWriteByte((u8)((WriteAddr)>>16)); 	//发送24bit地址    
	SPI3_ReadWriteByte((u8)((WriteAddr)>>8));   
	SPI3_ReadWriteByte((u8)WriteAddr);   
	for(i=0;i<NumByteToWrite;i++)SPI3_ReadWriteByte(pBuffer[i]);//循环写数  
	W25QXX_CS=1;                            	//取消片选 
	W25QXX_Wait_Busy();					   		//等待写入结束
} 
//无检验写SPI FLASH 
//必须确保所写的地址范围内的数据全部为0XFF,否则在非0XFF处写入的数据将失败!
//具有自动换页功能 
//在指定地址开始写入指定长度的数据,但是要确保地址不越界!
//pBuffer:数据存储区
//WriteAddr:开始写入的地址(24bit)
//NumByteToWrite:要写入的字节数(最大65535)
//CHECK OK
void W25QXX_Write_NoCheck(u8* pBuffer,u32 WriteAddr,u16 NumByteToWrite)   
{ 			 		 
	u16 pageremain;	   
	pageremain=256-WriteAddr%256; //单页剩余的字节数		 	    
	if(NumByteToWrite<=pageremain)pageremain=NumByteToWrite;//不大于256个字节
	while(1)
	{	   
		W25QXX_Write_Page(pBuffer,WriteAddr,pageremain);
		if(NumByteToWrite==pageremain)break;//写入结束了
	 	else //NumByteToWrite>pageremain
		{
			pBuffer+=pageremain;
			WriteAddr+=pageremain;	

			NumByteToWrite-=pageremain;			  //减去已经写入了的字节数
			if(NumByteToWrite>256)pageremain=256; //一次可以写入256个字节
			else pageremain=NumByteToWrite; 	  //不够256个字节了
		}
	};	    
} 
//写SPI FLASH  
//在指定地址开始写入指定长度的数据
//该函数带擦除操作!
//pBuffer:数据存储区
//WriteAddr:开始写入的地址(24bit)						
//NumByteToWrite:要写入的字节数(最大65535)   
u8 W25QXX_BUFFER[4096];
void W25QXX_Write(u8* pBuffer,u32 WriteAddr,u16 NumByteToWrite)
{ 
	u32 secpos = 0;
	u16 secoff = 0;
	u16 secremain = 0;
 	u16 i = 0;    
	u8 *W25QXX_BUF = W25QXX_BUFFER;
	
 	secpos = WriteAddr / 4096;//扇区地址  
	secoff = WriteAddr % 4096;//在扇区内的偏移
	secremain = 4096 - secoff;//扇区剩余空间大小

 	if(NumByteToWrite <= secremain)
	{
		secremain = NumByteToWrite;//不大于4096个字节
	}
	
	while(1) 
	{	
		W25QXX_CS=0;                            	//使能器件   
		SPI3_ReadWriteByte(W25X_ReadData);         	//发送读取命令   
		SPI3_ReadWriteByte((u8)((secpos*4096)>>16));  	//发送24bit地址    
		SPI3_ReadWriteByte((u8)((secpos*4096)>>8));   
		SPI3_ReadWriteByte((u8)(secpos*4096));   
		for(i=0;i<4096;i++)
		{ 
				W25QXX_BUF[i]=SPI3_ReadWriteByte(0XFF);   	//循环读数  
		}
		W25QXX_CS=1;
		
		W25QXX_Erase_Sector(secpos);		//擦除这个扇区
		for(i=0; i<secremain; i++)	   	//复制
		{
			W25QXX_BUF[i + secoff] = pBuffer[i];
		}
		
		if(secpos*4096 == SAVE_PROTECT_START_ADDRESS)
		{//保护区域写入校验字节数值
			W25QXX_BUF[4096 - 1] = SAVE_PROTECT_VALUE;
		}
		
		W25QXX_Write_NoCheck(W25QXX_BUF, secpos*4096, 4096);//写入整个扇区
		
		if(secpos*4096 == SAVE_PROTECT_START_ADDRESS)
		{
			W25QXX_Erase_Sector(SAVE_PROTECT_BACKUP_ADDRESS/4096);										//擦除保护区域的备份扇区
			W25QXX_Write_NoCheck(W25QXX_BUF, SAVE_PROTECT_BACKUP_ADDRESS, 4096);			//写入保护区域的备份扇区
		}
		
		if(NumByteToWrite == secremain)
		{
			break;//写入结束了
		}
		else//写入未结束
		{
			secpos++;//扇区地址增1
			secoff = 0;//偏移位置为0 	 

		  pBuffer += secremain;  				//指针偏移
			WriteAddr += secremain;				//写地址偏移	   
		  NumByteToWrite -= secremain;			//字节数递减
			if(NumByteToWrite > 4096)
				secremain = 4096;//下一个扇区还是写不完
			else 
				secremain = NumByteToWrite;		//下一个扇区可以写完了
		}	 
	}
}

